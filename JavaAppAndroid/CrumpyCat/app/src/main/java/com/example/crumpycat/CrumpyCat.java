package com.example.crumpycat;

public class CrumpyCat {
    private final String name;
    private final String image;

    public CrumpyCat(String name, String image) {
        this.name = name;
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public String getImage() {
        return image;
    }
}
